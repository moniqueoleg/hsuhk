                                            <h6>Air Transport Stream</h6>
                                            <p>Air transport industry has become more important to the global trade nowadays. Related industry sectors include airline, airport, air cargo terminal, air freight, express cargo and high value-added aviation services. With the continuous growth of air cargo throughput and the enhancement of transport and logistics infrastructure, demand for air transport professional increases dramatically. This study stream is designed for students who are interested in air transport and pursue a career in this area. It equips students with professional skills and knowledge of various sectors of air transport industry. Apart from taking the core modules, students are required to take some of the elective modules such as aviation security and safety management, shipping and aviation finance, sustainable transportation and carriage of goods law. Graduates will have a wide range of career options including air transport, cargo load planning, aviation safety and security, airline operations, traffic planning, trading and pricing analysis, airfreight operations, cargo terminal operations and aviation consultancy.</p>
