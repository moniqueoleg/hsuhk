                                            <h6>Business Logistics Stream</h6>
                                            <p>Logistics and trading industry has long been one of the four key engines driving Hong Kong economy for years. Therefore, managing logistics activities is indispensable to the development of the industry. Business logistics is a discipline of SCM with meeting customer requirements as its ultimate goal. It focuses on the management and integration of flows of goods, services, people, cash and information in the supply chain network that exists between the point of origin and the point of consumption. The mission of business logistics stream is to nurture innovative talents who can manage global supply chain operations with the state-of-art technology. Students will be equipped with the necessary technical and management know-how such as quality management, negotiation analysis, enterprise information management, supply management and service operations management. Graduates will have a wide range of career options in retail and merchandising management, transportation management, warehouse management, business analysis and digital solutions for supply chain management.         </p>
                                            <h6>Purpose of offering two streams</h6>
                                            <p>Driven by the surge of globalisation and the rigorous competition for both low cost and high innovativeness, enterprises today need to focus on strengthening their core competence while outsourcing the low value-added activities. In this connection, supply chain management (SCM) is the new form of business competitive model to tackle the trend. SCM is to manage and integrate customers, suppliers and delivery network with technology in order to provide customers with the products/services in the right quantity, right quality, right place, right time, and lowest cost. As the need for well-trained SCM graduates is increasing, the SCM programme is designed to cater for the long-term need by launching two study streams: Business Logistics and Air Transport.</p>
