                            <p>The Decision Sciences Technology Laboratory (DST Lab) - a facility with creative layout and the state-of-the-art software and hardware, to conduct research projects and gain hands-on laboratory experience The Decision Sciences Technology (DST) Laboratory was officially opened on 14 March 2013. The objectives of the DST Lab are to facilitate in-class discussions and promote collaborative learning.</p>
                            <p>The DST Lab is equipped with 32 sets of high performance Personal Computers and two High Definition resolution projectors. Down-viewed LCD monitors are installed to maximise usable space and increase the workplace flexibility. Student seats are arranged in a group of four to facilitate effective discussions and promote cooperative learning in group work.</p>
                            <p>Same as other classrooms in Block A, the DST Lab has the centralised presentation, lighting and AV system integrated in one LCD touch panel. Teachers can change the environment settings of the classroom to different modes like presentation modes, video modes, etc. with appropriate lamination to satisfy different needs.</p>
